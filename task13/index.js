// Завдання No13. Функція min ( a, b)
// Напишіть функцію min(a,b), яка повертає менше чисел a і b.
// Приклад викликів:
// min(2, 5) == 2
// min(3, -1) == -1
// min(1, 1) == 1
// **НЕ МОЖНА ВИКОРИСТОВУВАТИ СТАНДАРТНІ ФУНКЦІЇ ВИЗНАЧЕННЯ
// МАКСИМАЛЬНОГО ТА МІНІМАЛЬНОГО ЗНАЧЕНЬ

function min(a, b) {
  return a < b ? a : b;
}

console.log(min(1, -1));
